/**
 * The type of object represented by a `IconSize`
 */
export enum IconSize {
  /**
   * 90 pixel Icon image size identified via 90
   */
  PX90 = 'PX90',

  /**
   * 180 pixel Icon image size identified via PX180
   */
  PX180 = 'PX180',

  /**
   * 360 pixel Icon image size identified via PX360
   */
  PX360 = 'PX360',
}
